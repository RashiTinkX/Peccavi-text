"""
eval/plot_pareto.py
Plots the quality-detection Pareto frontier for PECCAVI, KGW, and SIR.

Reads:  results/pareto_data.json  (generated by scripts/pareto_sweep.py)
Writes: results/pareto_curve.png
        results/pareto_curve.pdf

Run from repo root:
    python eval/plot_pareto.py
"""

from __future__ import annotations
import json
import os
import sys
import math


def _valid(pt: dict) -> bool:
    auc = pt.get("auc_roc")
    ppl = pt.get("ppl_ratio")
    if auc is None or ppl is None:
        return False
    try:
        return not (math.isnan(float(auc)) or math.isnan(float(ppl)))
    except (TypeError, ValueError):
        return False


def main():
    pareto_path = "results/pareto_data.json"
    if not os.path.exists(pareto_path):
        print(f"ERROR: {pareto_path} not found.")
        print("Run  python scripts/pareto_sweep.py  first.")
        sys.exit(1)

    try:
        import matplotlib
        matplotlib.use("Agg")
        import matplotlib.pyplot as plt
    except ImportError:
        print("ERROR: matplotlib not installed. Run: pip install matplotlib")
        sys.exit(1)

    with open(pareto_path) as f:
        data = json.load(f)

    COLORS = {
        "kgw":     "#E05C5C",
        "sir":     "#5B9BD5",
        "peccavi": "#2CA02C",
    }
    LABELS = {
        "kgw":     "KGW (Kirchenbauer et al., 2023)",
        "sir":     "SIR (entropy-aware KGW)",
        "peccavi": "PECCAVI — learned θ (ours)",
    }

    fig, axes = plt.subplots(1, 2, figsize=(13, 5))

    # ── Left panel: AUC-ROC vs PPL ratio ──────────────────────────────────────
    ax = axes[0]
    for method in ("kgw", "sir"):
        pts = [p for p in data.get(method, []) if _valid(p)]
        if not pts:
            continue
        pts = sorted(pts, key=lambda p: p["ppl_ratio"])
        xs = [p["ppl_ratio"] for p in pts]
        ys = [p["auc_roc"] for p in pts]
        ax.plot(xs, ys, "o-", color=COLORS[method], linewidth=2,
                markersize=7, label=LABELS[method], zorder=2)
        for p in pts:
            ax.annotate(
                f"δ={p['delta']:.1f}",
                (p["ppl_ratio"], p["auc_roc"]),
                textcoords="offset points", xytext=(5, 4),
                fontsize=7.5, color=COLORS[method],
            )

    pec_pts = [p for p in data.get("peccavi", []) if _valid(p)]
    if pec_pts:
        px, py = pec_pts[0]["ppl_ratio"], pec_pts[0]["auc_roc"]
        ax.scatter([px], [py], marker="*", s=350, color=COLORS["peccavi"],
                   zorder=5, label=LABELS["peccavi"])
        theta_label = (
            f"θ={pec_pts[0]['theta']:.2f}" if pec_pts[0].get("theta") else "PECCAVI"
        )
        ax.annotate(
            theta_label, (px, py),
            textcoords="offset points", xytext=(8, -12),
            fontsize=9, color=COLORS["peccavi"], fontweight="bold",
        )

    # Target region guidelines
    ax.axvline(x=1.10, color="grey", linestyle="--", linewidth=1, alpha=0.6)
    ax.axhline(y=0.90, color="grey", linestyle="--", linewidth=1, alpha=0.6)
    ax.text(1.101, ax.get_ylim()[0] + 0.01 if ax.get_ylim()[0] else 0.5,
            "PPL ≤ 1.10", fontsize=7, color="grey", va="bottom")

    ax.set_xlabel("PPL Ratio (watermarked / baseline)  ←  lower is better", fontsize=10)
    ax.set_ylabel("AUC-ROC  →  higher is better", fontsize=10)
    ax.set_title("Quality–Detection Pareto Frontier", fontsize=12, fontweight="bold")
    ax.legend(loc="lower right", fontsize=8.5)
    ax.grid(True, alpha=0.3)

    # ── Right panel: GPT-4 attack survival vs PPL ratio ───────────────────────
    ax2 = axes[1]
    has_gpt4 = False
    for method in ("kgw", "sir"):
        pts = [p for p in data.get(method, [])
               if _valid(p) and p.get("gpt4_survival") is not None]
        if not pts:
            continue
        pts = sorted(pts, key=lambda p: p["ppl_ratio"])
        xs = [p["ppl_ratio"] for p in pts]
        ys = [p["gpt4_survival"] for p in pts]
        ax2.plot(xs, ys, "o-", color=COLORS[method], linewidth=2,
                 markersize=7, label=LABELS[method], zorder=2)
        has_gpt4 = True
        for p in pts:
            ax2.annotate(
                f"δ={p['delta']:.1f}",
                (p["ppl_ratio"], p["gpt4_survival"]),
                textcoords="offset points", xytext=(5, 4),
                fontsize=7.5, color=COLORS[method],
            )

    pec_g4 = [p for p in pec_pts if p.get("gpt4_survival") is not None]
    if pec_g4:
        px2, py2 = pec_g4[0]["ppl_ratio"], pec_g4[0]["gpt4_survival"]
        ax2.scatter([px2], [py2], marker="*", s=350, color=COLORS["peccavi"],
                    zorder=5, label=LABELS["peccavi"])
        has_gpt4 = True

    if has_gpt4:
        ax2.set_xlabel("PPL Ratio (watermarked / baseline)  ←  lower is better", fontsize=10)
        ax2.set_ylabel("Survival rate after GPT-4 paraphrase  →  higher is better", fontsize=10)
        ax2.set_title("Robustness to Neural Paraphrasing (GPT-4)", fontsize=12, fontweight="bold")
        ax2.legend(loc="lower right", fontsize=8.5)
        ax2.grid(True, alpha=0.3)
    else:
        ax2.set_visible(False)
        fig.set_size_inches(7, 5)

    fig.suptitle(
        "PECCAVI: Unified Adaptive Watermarking Framework\n"
        "KGW and SIR are fixed-θ special cases; PECCAVI learns the optimal operating point",
        fontsize=10, color="#444444", y=1.02,
    )
    fig.tight_layout()

    os.makedirs("results", exist_ok=True)
    for ext in ("png", "pdf"):
        path = f"results/pareto_curve.{ext}"
        fig.savefig(path, dpi=150, bbox_inches="tight")
        print(f"  Saved → {path}")

    plt.close(fig)
    print("\n  Include results/pareto_curve.pdf as Figure 1 in the paper.")


if __name__ == "__main__":
    main()
